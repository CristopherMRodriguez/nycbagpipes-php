				<p class="section-p blog-post-p">Long before Amazing Grace was composed, the highland bagpiper would play complicated pieces of music known as piobaireachd to honor an important person in the clan, therefore it is a longstanding Gaelic tradition to have a bagpiper play to honor the memory of the deceased. This class of tunes, to honor the memory of the dead, are known as laments.</p>
