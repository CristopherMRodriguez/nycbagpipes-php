				<h1 class="section-title blog-post-title">NYC Bagpipes' Piper Helps Band Receive 1st Place Award</h1>        
				<p class="section-p blog-post-p">On September 25th, 2021 NYC Bagpipes' bagpiper Cristopher Rodriguez helped the Sean McGonigal Memorial pipe band receive the 1st place award in the Grade 4 Pipe Band event held at the annual Celtic Classic festival in Bethlehem, Pennsylvania.  He played both the medley and the MSR events for the contest event.</p>
				<p class="section-p blog-post-p">Celtic Classic had 4 pipe bands compete this year; its first year back after the pandemic. Commonwealth Pipes & Drums earned 2nd place; and Novice Juvenile B World Champions Capital District Youth Pipe Band earned 3rd place.  The contest is an invitational event, allowing only certain pipe bands to be able to compete.</p>
				<p class="section-p blog-post-p">The 1st place victory follows another victory for the Sean McGonigal Memorial Pipe Band after playing the competition in Altamont, NY.  Their sister grade band St. Columcille Pipe Band (Grade 3) also won in the Grade 3 Pipe Band event, beating MacMillan-Birties Memorial Pipe Band.</p>
        <p class="section-p blog-post-p">Cristopher Rodriguez is the lead piper and author for an online bagpipe website made to help people hire a bagpiper and to learn how to play the bagpipes, called <a href="https://www.nycbagpipes.com">NYCBagpipes.com</a>.</p>
